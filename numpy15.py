import numpy as np

a=np.array([1,2,4])

print(np.exp(a)) # returns the exponential value (about equal to 2.7 ) of the array elements


print() # for new line

print(np.log(a) ) # returns the log(natural log) value of the array elements


print() # for new line

print(np.log10(a)) # returns the log10(base 10) value of the array elements