//constant example in rust
fn main(){
    const MY_CONST:i32=90; /*declaring constant by keyword "const"
    and also its data type explicitly */
    println!("First constant is {}",MY_CONST);
    //computing the area of the circle
    const PI:f32 = 3.14;
    let r=10.00; //radius 
    let area= PI*r*r ; //compute area of the circle
    println!("the area of the circle {}",area);




}