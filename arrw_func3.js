//arrow function with multiple statements
let myfun=(a,b)=>{
    sum=a+b
    sub=a-b
    mul=a*b
    div=a/b
    console.log("The addition is: ",sum)
    console.log("The subtraction is: ",sub)
    console.log("The multiplication is: ",mul)
    console.log("The division is: ",div)
}
//calling the arrow function
myfun(12,3)