//the arrow function
let a=()=>{console.log("Hello JavaScript")} //with no parameter

a() //calling the arrow function

let a1=(n)=>console.log("Your data is:",n) //with one parameter

a1(12) //calling the arrow function with numeric parameter
a1("hello JS ") //calling the arrow function  with string parameter
a1(14.5) //calling the arrow function with floating parameter
a1([123,45,44]) //calling the arrow function with array parameter
a1(('data',{'name':'Dhiraj','hobby':'coding'})) //calling the arrow function with map parameter
