//switch statement in dart
main(List<String> args) {
  

  int n=10; //variable initialisation

  switch(n)
  {
    case 1:
    print("value is 1 ");
    break;

    case 2:
    print("value is 2 ");
    break;

    case 3:
        print("value is 3 ");
        break;

    case 4:
        print("value is 4 ");
        break;
      default:
      print("This is default value :)");

    
  }
}