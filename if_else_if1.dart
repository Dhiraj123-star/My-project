//if else if example
main(List<String> args) {
  
  //variable declaration
  var marks=78;
  if(marks>85){
    print("Excellent");

  }

  else if(marks>75){
    print("very good");
  }
  else if(marks>65){
    print(" good");
  }
  else if(marks>55){
    print("Average");
  }
  else{
    print("Sorry, Fail");
  }


}