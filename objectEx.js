//object example in js
const people={
    name:"Dhiraj",
    id:121,
    age:23,
    hobby:"Coding"

}
//accessing elements of the object
console.log("The name of the person:",people.name)
console.log("The age of the person: ",people.age)
console.log("The hobby of the person is: ",people.hobby)
console.log("The id of the person is: ",people.id)
//another way of accessing property by using bracket
console.log("using bracket :",people["age"]) //returns 23
console.log("Thanks for using javaScript :)")
